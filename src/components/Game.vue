<template>
  <div class="game">
    <div class="game-container">
      <h1>beat<br>the<br><strong>glock</strong></h1>
      <h5>Round {{ game.round }}</h5>
      <div v-if="game.step =='guess' && !game.winner" class="timer">{{ timer }}</div>

      <svg width="600" height="300">
        <key
          v-for="(settings, note) in notes"
          v-on:play="userPlaysNote"
          :id="note"
          :key="note"
          :note="settings"
        />
      </svg>

      <p v-if="!game.winner">
        <span v-if="game.userTurn != user.username">Nice moves. Waiting on {{ challenger.username }} ...</span>

        <span v-else>
          <span v-if="game.step == 'listen'">{{ challenger.username }} has made a move.</span>

          <span v-else-if="game.step == 'add'">
            <span v-if="game.round == 1">Add the first note.</span>
            <span v-else>Add another note.</span>
          </span>

          <span v-else-if="game.step == 'guess'">Now play it back.</span>
        </span>
      </p>

      <spinner v-if="!game.winner && game.userTurn != user.username"></spinner>

      <transition name="gameover">
        <div v-if="game.winner">
          <h5 v-if="game.winner == challenger.username" class="gameover">GAMEOVER</h5>
          <h5 v-else class="winner">WINNER</h5>
          <p>
            <strong>{{ game.winner }} wins.</strong>
          </p>
        </div>
      </transition>

      <div class="actions">
        <button
          class="btn"
          v-if="game.userTurn == user.username && game.step == 'listen'"
          @click="listen()"
        >Listen</button>
      </div>

      <!-- TEMP AI ACTIONS -->
      <button v-if="game.userTurn == challenger.username && !game.winner" class="btn ai" @click="aiMove()">AI MOVE</button>

      <audio v-for="(settings, note) in notes" :id="note" preload="auto" :key="note">
        <source :src="settings.sound" type="audio/wav" />
      </audio>
    </div>
  </div>
</template>

<script>
import { default as Key } from "./Key";
import { default as notes } from "./../config/notes";
import { default as Spinner } from "./../../node_modules/vue-spinners/src/components/CubeSpinner";
import { setTimeout } from "timers";

export default {
  props: {
    game: {
      type: Object
    }
  },
  components: {
    Key,
    Spinner
  },
  data() {
    return {
      guesses: [],
      notes: notes,
	  challenger: null,
	  user: null,
      speed: 1000,
      timer: 0
    };
  },
  created() {
    if (this.game.mode == "hard") {
      this.speed = 500;
    }

	this.user = this.game.player1.id == this.$socket.client.id ? this.game.player1 : this.game.player2;
	this.challenger = this.game.player1.id == this.$socket.client.id ? this.game.player2 : this.game.player1;
  },
  computed: {
    step() {
      return this.game.step;
    }
  },
  watch: {
    step(next, prev) {
      console.log(next);
    }
  },
  methods: {
    userPlaysNote(note) {
      if (this.game.userTurn == this.user.username) {
        if (this.game.step == "guess") {
          this.guesses.push(note);
          this.play(note);

          if (
            this.guesses[this.guesses.length - 1] !=
            this.game.melody[this.guesses.length - 1]
          ) {
            console.log("guesses", this.guesses);
            console.log("melody", this.game.melody);
            this.game.winner = this.challenger.username;
          }

          if (this.game.melody.length == this.guesses.length) {
            this.submitGuess();
          }
        } else if (this.game.step == "add") {
          this.game.melody.push(note);
          this.play(note);

          // End turn, pass to challenger
          this.game.userTurn = this.challenger.username;
          this.game.round++;
          this.game.step = "listen";
        }
      }
    },
    aiMove() {
      let randNum = Math.floor(Math.random() * 10);

      if (randNum > (this.game.mode == "hard" ? 9 : 5)) {
        this.game.winner = this.user.username;
      } else {
        this.game.melody.push(this.getRandomNote());
        this.game.round++;
        this.game.userTurn = this.user.username;
        this.game.step = "listen";
      }
    },
    submitGuess() {
      let correct = true;

      this.game.melody.forEach((note, i) => {
        if (note != this.guesses[i]) {
          correct = false;
        }
      });

      correct ? (this.game.step = "add") : (this.game.winner = this.challenger.username);

      this.guesses = [];
    },
    play(note) {
      if (
        this.game.mode == "easy" ||
        ["guess", "add"].includes(this.game.step)
      ) {
        this.lightKey(note);
      }
      this.playSound(note);
    },
    playSound(note) {
      let audio = document.getElementById(note);

      audio.pause();
      audio.currentTime = 0;
      audio.play();
    },
    lightKey(note) {
      this.notes[note].lit = true;
      setTimeout(() => (this.notes[note].lit = false), 400);
    },
    reset() {
      this.clearMelody();
      this.clearGuesses();
    },
    listen() {
      this.playMelody(this.speed);
      this.game.step = null;

      setTimeout(() => {
        this.game.step = "guess";
        let timer = parseInt(
          (5000 + this.speed * this.game.melody.length * 2) / 1000
        );

        console.log("timer", timer);
        this.startTimer(timer);
      }, this.game.melody.length * this.speed + 1000);
    },
    startTimer(num) {
      let vm = this;
      vm.timer = num;
      let countdown = setInterval(function() {
        vm.timer = vm.timer - 1;

        if (vm.game.step == "add") {
          clearInterval(countdown);
        } else if (vm.timer < 1) {
          clearInterval(countdown);
          vm.game.winner = vm.challenger;
        }
      }, 1000);
    },
    getRandomNote() {
      let notes = ["c", "d", "e", "f", "g", "a", "b", "c2"];
      let randIndex = Math.floor(Math.random() * 8);
      return notes[randIndex];
    },
    playMelody(speed) {
      let x = 0;

      let intervalId = setInterval(() => {
        if (!this.game.melody[x]) {
          clearInterval(intervalId);
        } else {
          this.play(this.game.melody[x]);
        }

        x++;
      }, speed);
    }
  }
};
</script>

